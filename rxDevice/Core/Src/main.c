typedef struct {
	int x;
	int y;
} coords;

long map(long x, long in_min, long in_max, long out_min, long out_max) {
  return (x - in_min) * (out_max - out_min) / (in_max - in_min) + out_min;
}

coords convertRotationToCoords(int rotation) {
	coords c;

	double degree = map(rotation, 25, 125, 0, 180) * 3.14159/180;

	c.x = 63 + 64 * cos(degree);
	c.y = 63 - 63 * sin(degree);

	return c;
}

coords convertDistanceToCoords(int distance, int rotation) {
	coords c;

	double degree = map(rotation, 25, 125, 0, 180) * 3.14159/180;
	int d = map(distance, 0, THRESHOLD, 0, 64);

	c.x = 63 + d * cos(degree);
	c.y = 63 - d * sin(degree);

	return c;
}
