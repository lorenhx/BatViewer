void TIM6_Callback() {
	HAL_GPIO_WritePin(GPIOA, GPIO_PIN_1, GPIO_PIN_SET); // alza Trigger
	HAL_TIM_Base_Start_IT(&htim4);

	HAL_TIM_Base_Stop_IT(&htim6);
	htim6.Instance->CNT = 0;
}

void TIM2_Callback() {
	HAL_TIM_Base_Start_IT(&htim6);

	if(direction == 0) {
		rotation += 5;
		htim1.Instance->CCR1 = rotation;

		if(rotation == 125) {
			direction = 1;
		}
	} else {
		rotation -= 5;
		htim1.Instance->CCR1 = rotation;

		if(rotation == 25) {
			direction = 0;
		}
	}
}

void TIM4_Callback() {
	HAL_GPIO_WritePin(GPIOA, GPIO_PIN_1, GPIO_PIN_RESET); // abbassa Trigger

	HAL_TIM_Base_Stop_IT(&htim4);
	htim4.Instance->CNT = 0;
}

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef* htim) {
	if(htim == &htim6) {
		TIM6_Callback();
	} else if(htim == &htim2) {
		TIM2_Callback();
	} else if(htim == &htim4) {
		TIM4_Callback();
	}
}
void ECHO_Callback() {
	if(HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_2) == GPIO_PIN_SET) {	// rising-edge
		HAL_TIM_Base_Start_IT(&htim3);
	} else {	// falling-edge
		HAL_TIM_Base_Stop_IT(&htim3);
		if(htim3.Instance->CNT > 25000) {
			distance = MAX_UINT_16;
		} else {
			distance = htim3.Instance->CNT / 58;
		}

		sendbuff[0] = distance;
		sendbuff[1] = rotation;
		HAL_UART_Transmit_IT(&huart4, (uint8_t *) sendbuff, sizeof(uint16_t) * DATA_SIZE);
		htim3.Instance->CNT = 0;
	}
}

void BUTTON_Callback() {
	if(paused) {
		HAL_TIM_Base_Start_IT(&htim2);
		HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_1);
		paused = 0;
	} else {
		HAL_TIM_Base_Stop_IT(&htim2);
		HAL_TIM_PWM_Stop(&htim1, TIM_CHANNEL_1);
		paused = 1;
	}
}

void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin) {
	if(GPIO_Pin == GPIO_PIN_2) {
		ECHO_Callback();
	} else if(GPIO_Pin == GPIO_PIN_0) {
		BUTTON_Callback();
	}
}

void HAL_UART_TxCpltCallback(UART_HandleTypeDef *huart) {
	HAL_GPIO_WritePin(GPIOE, GPIO_PIN_8, GPIO_PIN_SET);
}

